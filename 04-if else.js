/* 
if = se
else = se não
else if = mas se
*/
let idade = 18;
if (idade >= 18) {
  console.log("maior de idade");
} else {
  console.log("não e de maior");
}

/**** Exemplo de if, else, else if ****/
let temperatura = 35;
if (temperatura >= 36 && temperatura <= 38) {
  console.log("ok");
} else if (temperatura > 38) {
  console.log("febre");
} else {
  console.log("baixa temperatura");
}
